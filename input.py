#Que el usuario introduzca tres numero y nos diga el mayor y el menor

num1 = int(input("Introduce el primer numero: "))

num2 = int(input("Introduce el segundo numero: "))

num3 = int(input("Introduce el tercer numero: "))

print("El mayor de los números que has introducido es",max(num1,num2,num3))

print("El menor de los número introducidos es ",min(num1,num2,num3))

